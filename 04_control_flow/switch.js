const month = "dec";
switch (month) {
    case "aug":
        console.log("August")
        break;
    case "july":
        console.log("July")
        break;
    case "april":
        console.log("April")
        break;
    case "dec":
        console.log("December")
        break;
    case "sept":
        console.log("September")
        break;

    default:
        console.log("default case achieved")
        break;
}